installGlobalErrorHandlers();

// VERSION is sourced from src/build-info.ts (auto-generated by
// scripts/build.mjs from package.json). Reading package.json at runtime
// would break under `bun build --compile` because the source paths
// don't exist in the bundled binary's virtual filesystem.
export const program = new Command()
  .name("switchroom")
  .description(
    "Multi-agent orchestrator for Claude Code. One Telegram group, many specialized agents."
  )
  .version(VERSION)
  .option("-c, --config <path>", "Path to switchroom.yaml config file")
  .hook("preAction", async (_thisCommand, actionCommand) => {
    await captureEvent("cli_command_invoked", {
      command: actionCommand.name(),
      version: VERSION,
      node_version: process.version,
      platform: process.platform,
    });
  });

registerSetupCommand(program);
